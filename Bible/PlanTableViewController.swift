import UIKit

class PlanTableViewController: UITableViewController {
    
    @IBOutlet weak var datePicker: UIDatePicker!
    
    var bible: Bible?
    var chapters = [Chapter]()

    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
        // self.navigationItem.rightBarButtonItem = self.editButtonItem
        if let bible = bible {
            chapters = bible.getPlan()
            datePicker.date = bible.getDeadline()
        }
    }

    @IBAction func onDeadlineChange(_ sender: UIDatePicker) {
        if let bible = bible {
            bible.setDeadline(sender.date)
            chapters = bible.getPlan()
            tableView.reloadData()
        }
    }
    
    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return chapters.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "PlanTVC", for: indexPath)

        // Configure the cell...
        let chapter = chapters[indexPath.row]
        cell.textLabel?.text = chapter.getString()
        
        if chapter.isMarkedRead() {
            cell.accessoryType = .checkmark
        } else {
            cell.accessoryType = .none
        }

        return cell
    }

